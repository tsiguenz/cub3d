#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new_)
{
	t_list	*tmp;

	if (! *lst)
	{
		*lst = new_;
		return ;
	}
	tmp = *lst;
	while (tmp->next)
		tmp = tmp->next;
	tmp->next = new_;
	new_->prev = tmp;
}
