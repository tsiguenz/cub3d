#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*res;
	size_t	len;
	size_t	i;

	if (!s)
		return (NULL);
	res = ft_strdup(s);
	if (!res)
		return (NULL);
	len = ft_strlen(s);
	i = 0;
	while (i < len)
	{
		res[i] = f(i, res[i]);
		i++;
	}
	return (res);
}
